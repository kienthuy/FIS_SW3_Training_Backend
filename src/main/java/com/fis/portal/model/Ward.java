package com.fis.portal.model;

public class Ward {
	private String code;
	private String name;
	private String nameEn;
	private String unit;
	private String districtCode;
	private String districtName;
	private String provinceCode;
	private String provinceName;
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNameEn() {
		return nameEn;
	}
	public void setNameEn(String nameEn) {
		this.nameEn = nameEn;
	}
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getdistrictCode() {
		return districtCode;
	}
	public void districtCode(String districtCode) {
		this.districtCode = districtCode;
	}
	public String getdistrictName() {
		return districtName;
	}
	public void districtName(String districtName) {
		this.districtName = districtName;
	}
	public String getprovinceCode() {
		return provinceCode;
	}
	public void provinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}
	public String getprovinceName() {
		return provinceName;
	}
	public void provinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	
	
}
